require 'pendaftaran/db_connect.php'; // File koneksi ke database

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = $_POST['username'];
    $password = $_POST['password'];

    // Query database untuk verifikasi username dan password
    $query = "SELECT * FROM tblMahasiswa WHERE mahasiswa_nim = ? AND password = ?";
    $stmt = $conn->prepare($query);
    $stmt->bind_param('ss', $username, $password);
    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        // Login berhasil
        $user = $result->fetch_assoc();
        $_SESSION['username'] = $user['mahasiswa_nim'];
        $_SESSION['nama'] = $user['mahasiswa_nama'];
        header("Location: tabel_mahasiswa.php"); // Arahkan ke tabel mahasiswa
        exit();
    } else {
        // Login gagal
        $error = 'Username atau Password salah.';
    }
